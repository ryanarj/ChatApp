public class Color {

    public String r;
    public String g;
    public String b;

    public Color(){

    }

    public Color(String r, String g, String b){
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public void setR(String r){
        this.r = r;
    }
    public void setG(String g){ this.g = g;}
    public void setB(String b){
        this.b = b;
    }
}
